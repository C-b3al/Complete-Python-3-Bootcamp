#### comparisons and logical operators
# comparisons between variables depend on:
# 1. similarity
# 2. type 

# possible to chain operations together
print(1<2<3)
# can use inbuilt functionality keywords to return booleans
print(1<2 and 2<3) # both need to be true to return true
print(1==2 or 2<3) # only one needs to be true to return true
